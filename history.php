	<section class="aboutpup">

		<p>
		Upon the invitation of the Export Processing Authority, the Polytechnic University of the Philippines, then the Philippine College of Commerce (PCC) under the leadership of Pres. Isabelo T. Crisostomo, decided to establish a branch in BEPZ and henceforth created an ad hoc committee chaired by the late Dean Adelaida B. Zamora to resume negotiation with the Export Processing Zone Authority (EPZA) as regards establishing a branch of the college in Bataan. The project was meant not only to provide educational facilities to the children of residents of the community but also to respond to the call of the government for the dispersal of schools and decongestion of Metro Manila. This was also PCC's way of bringing quality education to the masses in the countryside.
		</p>
		<br>
		<p>
		The proposal to establish a PCC branch in BEPZ was approved by the EPZA Board of Commissioners in its meeting on June 16, 1976. This was subsequently approved by the PCC Board of Trustees during its meeting on June 19, 1976. The Export Processing Zone Authority through its Chairman-Administrator, Atty. Teodoro Q, Pena, assigned a three-hectare vacant lot in the center of the industrial sector for the temporary site of the Branch.
		</p>
		<br>
		<p>
		In 1981, BEPZ authorities transferred the University Branch to the former BEPZ Police Headquarters along the national road.
		</p>
		<br>
		<p>
		The progenitor of the University Branch at the Bataan Export Processing Zone, in terms of buildings and secondary school personnel was the National Shipyard and Steel Corporation (NASSCO) Barrio High School, which was established on July 19, 1966 in NASSCO area through the efforts of Barrio Captain Manuel Basas.
		</p>
		<br>
		<p>
		Four years later, on March 1970, the Mariveles Municipal Council enacted a resolution converting NASSCO Barrio High School to Mariveles Municipal High School.
		</p>
		<br>
		<p>
		On February 19, 1973, Bataan Export Processing Zone Authority took over the financing of the school and renamed it BEPZ High School. Later, financing and supervision were turned over to the Philippine College of Commerce.
		</p>
		<br>
		<p>
		The formal opening of the Branch was made on July 1, 1976 with the presence of Zone Manager, Atty. Dioscorro Manrique and Pres. Isabelo T. Crisostomo, representing the Authority and the College, respectively.
		</p>
		<br>
		<p>
		The Branch was placed under the overall supervision of Dean Adelaida B. Zamora, the first Officer-in-Charge of the Branch. After her death on August 4, 1978, Executive Dean Raymundo G. Garcia was designated Acting Officer-in-Charge.
		</p>
		<br>
		<p>
		On November 3, 1978, the authority and responsibility for the BEPZ Branch was turned over to Dr. Pedro P. Mendonez, who later resigned at the close of the Academic Year 1978-1979.
		</p>
		<br>
		<p>
		When Dr. Nemesio E. Pridente assumed the PUP Presidency on April 20, 1986, Dr. Mirriam A. Padolina was designated Branch Officer-in Charge on June 13, 1986.
		</p>
		<br>
		<p>
		On July 4, 1988, Dr. Samuel M. Salvador took over as Branch Director. He introduced corrective measures aimed at improving the administrative procedures and means of attaining efficiency in the delivery of services. Dr. Salvador was promoted and became the Vice-President for Academic Affairs.
		</p>
		<br>
		<p>
		Dr. Pedro Yunzal was designated Director on April 3, 1989. A creative researcher, he initiated the establishment of a Training and Research Center for students and faculty.
		</p>
		<br>
		<p>
		In 1990, Dr. Romeo Q. Mendoza was given the authority to run the Branch but after a year, he went on an indefinite leave.
		</p>
		<br>
		<p>
		Dr. Amelia C. Lapira assumed directorship on August 1, 1992. She worked for the offering of the programs Master in Education Management â€“ Distance Learning System and Bachelor in Secondary Education. Various physical improvements were made during her administration. She was also responsible for paving the way for PUP and PEZA to negotiate the donation of the former BEPZ Medical Center to PUP as the permanent site of the University Branch.
		</p>
		<br>
		<p>
		With the retirement from service of Dr. Amelia C. Lapira, Dr. Loreto V. Jao was installed as the Branch Director on October 12, 1999. It is during her incumbency that the transfer of PUP Bataan to its new site in Barangay Malaya was facilitated. She subjected the newly renovated building to a complete face-lifting, focusing on cleanliness, proper maintenance and beautification. Rehabilitation of rooms to become the library, the audio-visual room, and the computer center was made during her term with the assistance of Cong. Enrique “Tet” Garcia and Sen. Ramon Magsaysay.
		</p>
		<br>
		<p>
		In 2005, Dr. Marietta Z. De Guzman assumed position of directorship upon retirement of Dr. Jao. But after a year, Prof. Thelma D. Escalada took over as the Branch Director. Through Prof. Escalada's efforts, the Provincial Government of Bataan grant started granting the tuition fee refund program with the undergraduate students. It was also during her term when the Speech Laboratory equipment was made available and the Bachelor of Elementary Education program was offered in the Branch.
		</p>
		<br>
		<p>
		After three years, PUP Bataan's responsibility was transferred under the leadership of Prof. Leonila J. Generales, the present Director of the Branch. During her first few months, the installation of the Speech Laboratory was finished and thirty (30) personal computers were donated by Gov. Enrique “Tet” Garcia. The Branch Library became a wi-fi zone, together with the Computer Laboratory.
		</p>
		<br>
		<p>
		These milestones of the Branch mark the progressive development of the Branch, as well as the passion of the people behind these accomplishments in bringing out the best from its students.
		</p>

